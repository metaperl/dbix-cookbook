package DBIx::Cookbook;

use warnings;
use strict;

our $VERSION = '0.01';


=head1 NAME

DBIx::Cookbook - executable cookbook code for DBI ORMs

=head1 VERSION

Version 0.01

=cut


=head1 SYNOPSIS

  dbicook database install

  dbicook dbic loader 
  dbicook dbic fetch_all

  dbicook database reset

  dbicook skinny loader
  dbicook skinny fetch_all




